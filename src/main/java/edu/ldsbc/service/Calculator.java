public class Calculator {

    public int add (int num1, int num2){
        return num1 + num2;
    }
    public int subtraction (int num3, int num4){
        return num3 - num4;
    }

    public int divide (int num5, int num6){
        return num5 / num6;
    }

    public int multiply (int num7, int num8){
        return num7 * num8;
    }
}
